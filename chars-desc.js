/* ===== ユーティリティ =====
   DESC を使ってHTML文字列を生成するヘルパー。
   index.html / partner.html の結果表示部分から呼び出す想定。 */

/**
 * タイプコードを受け取り、結果文章のHTML断片を返す。
 * @param {string} code - タイプコード（例: "AFED"）
 * @returns {string} HTML文字列
 */
function descHTML(code) {
  const d = DESC[code];
  if (!d) return '';

  const traitsHTML = d.traits
    .map(t => `<li>${t}</li>`)
    .join('');

  return `
    <p class="desc-intro">${d.intro}</p>
    <div class="desc-block">
      <h3 class="desc-label">あるある</h3>
      <ul class="desc-traits">${traitsHTML}</ul>
    </div>
    <div class="desc-block">
      <h3 class="desc-label">強み</h3>
      <p class="desc-strength">${d.strength}</p>
    </div>
    <div class="desc-block">
      <h3 class="desc-label">気をつけたいこと</h3>
      <p class="desc-caution">${d.caution}</p>
    </div>
  `.trim();
}
